import UIKit

extension UIColor {
    static var darkMidnightBlueColor: UIColor {
        return UIColor(named: "dark.midnight.blue") ?? .blue
    }
    
    static var darkPurpleColor: UIColor {
        return UIColor(named: "dark.purple") ?? .purple
    }
    
    static var deepNightBlueColor: UIColor {
        return UIColor(named: "deep.night.blue") ?? .blue
    }
    
    static var softBlueColor: UIColor {
        return UIColor(named: "soft.blue") ?? .blue
    }
}
